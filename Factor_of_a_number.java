package practice_question_java;


public class Factor_of_a_number {
    public static void main(String[]args)
    {
        int num=12;
        for(int i=1;i<=num;i++)
        {
        if(num%i==0)
        {
            System.out.print(i+" ");
            
        }
    }
    }
}
